;----------------------------------------------------------------------------------------------------
; Does NOP or does nothing (for debug purposes)
;----------------------------------------------------------------------------------------------------
; Entry:	None
; Exit:		None
; Destr:	None
;----------------------------------------------------------------------------------------------------
.do_nop		macro
		nop
		endm
;----------------------------------------------------------------------------------------------------

;----------------------------------------------------------------------------------------------------
; Exits program to DOS
;----------------------------------------------------------------------------------------------------
; Entry: AL or <code> - exit code
; Exit:  N/A
; Destr: Everything
;----------------------------------------------------------------------------------------------------
.exit_program  	macro code
		.do_nop

	ifnb <code>
		mov		ax,		4C00h or code
	else
		mov		ah,		4C
	endif
	
		int		21h

		.do_nop
		endm
;----------------------------------------------------------------------------------------------------
